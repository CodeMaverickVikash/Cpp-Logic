/*
* Defination: The word polymorphism means having many forms. In simple words, we can define polymorphism as the ability of a message to be displayed in more than one form.

In C++ polymorphism is mainly divided into two types:
1. Compile time Polymorphism
2. Runtime Polymorphism

1. Compile time polymorphism: This type of polymorphism is achieved by function overloading or operator overloading.
2. Runtime polymorphism: This type of polymorphism is achieved by Function Overriding.
Function overriding is redefinition of base(parent) class function in its derived(child) class with same signature i.e return type and parameters.
*/